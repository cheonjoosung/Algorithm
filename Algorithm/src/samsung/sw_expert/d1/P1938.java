package samsung.sw_expert.d1;

import java.util.Scanner;

//간단한 계산기
public class P1938 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		sc.close();
	}
}